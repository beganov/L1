package main

import "fmt"

// L1.19
// –†–∞–∑—Ä–∞–±–æ—Ç–∞—Ç—å –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –ø–µ—Ä–µ–≤–æ—Ä–∞—á–∏–≤–∞–µ—Ç –ø–æ–¥–∞–≤–∞–µ–º—É—é –Ω–∞ –≤—Ö–æ–¥ —Å—Ç—Ä–æ–∫—É.
// –ù–∞–ø—Ä–∏–º–µ—Ä: –ø—Ä–∏ –≤–≤–æ–¥–µ —Å—Ç—Ä–æ–∫–∏ ¬´–≥–ª–∞–≤—Ä—ã–±–∞¬ª –≤—ã–≤–æ–¥ –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å ¬´–∞–±—ã—Ä–≤–∞–ª–≥¬ª.
// –£—á—Ç–∏—Ç–µ, —á—Ç–æ —Å–∏–º–≤–æ–ª—ã –º–æ–≥—É—Ç –±—ã—Ç—å –≤ Unicode (—Ä—É—Å—Å–∫–∏–µ –±—É–∫–≤—ã, emoji –∏ –ø—Ä.),
// —Ç–æ –µ—Å—Ç—å –ø—Ä–æ—Å—Ç–æ iterating –ø–æ –±–∞–π—Ç–∞–º –º–æ–∂–µ—Ç –Ω–µ –ø–æ–¥–æ–π—Ç–∏ ‚Äî –Ω—É–∂–µ–Ω —Å—Ä–µ–∑ —Ä—É–Ω ([]rune).

//–¥–æ–±–∞–≤–∏–ª —ç–º–æ–¥–∑–∏ –¥–ª—è –¥–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏–∏ —Ä–∞–±–æ—Ç—ã —Å –¥—Ä—É–≥–∏–º —Ä–∞–∑–º–µ—Ä–æ–º –±–∞–π—Ç–∞
var mainString = "–≥–ª–∞–≤—Ä—ã–±–∞üêü"

func main() {
	runeArr := []rune(mainString)
	for i := 0; i < len(runeArr)/2; i++ { //–ø—Ä–æ—Ö–æ–¥–∏–º –¥–æ —Å–µ—Ä–µ–¥–∏–Ω—ã —Å—Ç—Ä–æ–∫–∏
		runeArr[i], runeArr[len(runeArr)-1-i] = runeArr[len(runeArr)-1-i], runeArr[i]
		//–º–µ–Ω—è–µ–º –º–µ—Å—Ç–∞–º–∏ –∑–µ—Ä–∫–∞–ª—å–Ω—ã–µ –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ —Ü–µ–Ω—Ç—Ä–∞ —ç–ª–µ–º–µ–Ω—Ç—ã
	}
	mainString = string(runeArr)
	fmt.Println(mainString)
}
